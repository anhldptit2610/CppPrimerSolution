/*
    Exercise 6.49: What is a candidate function? What is a viable function?
*/

/* solution */

/*
    A candidate function is a function which is the candidate for the
    function call. It must match the name.
    (A candidate function is the function which in the set of
    overloaded functions considered for the call)

    A viable function is candidate function in which they have the
    same type and amount of arguments as the called function.
*/